import SwiftUI


extension Font {
    
    static func FuturaMedium(size: CGFloat) -> Font {
        Font.custom("Futura-Medium", size: size)
    }
    
    static func FuturaMediumItalic(size: CGFloat) -> Font {
        Font.custom("Futura-MediumItalic", size: size)
    }
    
    static func FuturaBold(size: CGFloat) -> Font {
        Font.custom("Futura-Bold", size: size)
    }
    
    static func PoppinsLatinBold(size: CGFloat) -> Font {
        Font.custom("PoppinsLatin-Bold", size: size)
    }
    
    static func PoppinsLatinExtraBold(size: CGFloat) -> Font {
        Font.custom("PoppinsLatin-ExtraBold", size: size)
    }
    
    static func PoppinsLatinExtraLight(size: CGFloat) -> Font {
        Font.custom("PoppinsLatin-ExtraLight", size: size)
    }
    
    static func PoppinsLatinLight(size: CGFloat) -> Font {
        Font.custom("PoppinsLatin-Light", size: size)
    }
    
    static func PoppinsLatinMedium(size: CGFloat) -> Font {
        Font.custom("PoppinsLatin-Medium", size: size)
    }
    
    static func PoppinsLatin(size: CGFloat) -> Font {
        Font.custom("PoppinsLatin-Regular", size: size)
    }
    
    static func VAGRoundedNextBlack(size: CGFloat) -> Font {
        Font.custom("VAGRoundedNext-Black", size: size)
    }
    
    static func VAGRoundedNextMedium(size: CGFloat) -> Font {
        Font.custom("VAGRoundedNext-Medium", size: size)
    }
    
}
